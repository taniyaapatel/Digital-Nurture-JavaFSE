
public class TypeCasting {
    public static void main(String[] args) {
        double d = 88.75;
        int i = (int) d;
        System.out.println("Double value: " + d);
        System.out.println("Casted to int: " + i);

        int x = 45;
        double y = (double) x;
        System.out.println("Int value: " + x);
        System.out.println("Casted to double: " + y);
    }
}
